// Arithmetics Operators

// Multiplication
    console.log(25.5 * 10)
// Division
    console.log(10 / 5)
//Sum
    console.log(10 + 10)
// Subtract
    console.log(10 - 9)

// Remainder (Módulo da função ou o resto)
    let remainder = 11 % 9
    console.log(remainder)

// Increment
    let increment = 0
    increment++
    console.log(++increment)

// Decrement
    let decrement = 5
    decrement--
    console.log(--decrement)
    
// Exponencial
console.log(3 ** 2)
