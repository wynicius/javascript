// Logical Operators

// -2 valores booleanos, quando verificados, resultará em verdadeiro ou falso.

// Seguem a mesma lógica dos conectores lógicos em raciocínio lógico!

let pao = true
let queijo = false

// AND -> &&
console.log(pao && queijo)

// OR -> ||
console.log(pao || queijo)

// NOT -> !
console.log( !pao )